void CMainFrame::MessageToBar(LPCTSTR msg, UINT id)
{
	switch (id) {
	case IDS_STATUS_PANE1:
		m_wndXHairPane->SetText(msg);
		m_wndXHairPane->Redraw();
		break;
	case IDS_STATUS_PANE2:
		m_wndDiffPane->SetText(msg);
		m_wndDiffPane->Redraw();
		break;
	}
}
